function normalize(v) {
  const sum = v.reduce((s, x) => s + x, 0);
  return v.map((x) => x / sum);
}

function dot(a, b) {
  return a.reduce((s, x, i) => s + x * b[i], 0);
}

function norm(a) {
  return Math.sqrt(dot(a, a));
}

function cosine(a, b) {
  const na = norm(a);
  const nb = norm(b);
  return na && nb ? dot(a, b) / (na * nb) : 0;
}

